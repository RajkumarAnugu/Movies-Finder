package com.raulh82vlc.MoviesFinder.ui.widgets;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.ImageView;

/**
 * Proportional view which enables a 20% increased view height compared with the width

 * @author Raul Hernandez Lopez
 * on 30/04/2016.
 */
public class ProportionalImageView extends ImageView {

    private static final double PERCENTAGE_TO_INCREASE = 1.2;

    public ProportionalImageView(Context context) {
        super(context);
    }

    public ProportionalImageView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public ProportionalImageView(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    @Override
    protected void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {
        super.onMeasure(widthMeasureSpec, heightMeasureSpec);

        int width = getMeasuredWidth();
        int height = (int) (width * PERCENTAGE_TO_INCREASE);
        setMeasuredDimension(width, height);
    }
}
